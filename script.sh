#!/bin/bash

# 引数の数をチェック
if [ $# -ne 2 ]; then
    echo "右記のように指定してください: $0 自然数1 自然数2"
    exit 1
fi

a=$1
b=$2

# 自然数かどうかをチェック（1以上）
if ! [[ "$a" =~ ^[1-9][0-9]*$ && "$b" =~ ^[1-9][0-9]*$ ]]; then
    echo "エラー: 両方の引数は自然数（1以上の整数）でなければなりません"
    exit 1
fi

# ユークリッドの互除法
while [ "$b" -ne 0 ]; do
    temp=$b
    b=$((a % b))
    a=$temp
done

echo "最大公約数は: $a"

